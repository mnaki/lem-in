#include "libft.h"

int			ft_iscntrl(int c)
{
	if (c == '\a'
		|| c == '\b'
		|| c == '\t'
		|| c == '\n'
		|| c == '\v'
		|| c == '\f'
		|| c == '\r'
		|| c == '\033'
		|| c == ' ')
		return (1);
	return (0);
}
